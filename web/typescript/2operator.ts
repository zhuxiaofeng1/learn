let num1:number = 10;
let num2:number = 20;
console.log("num1 :", num1);
console.log("num2 :", num2);
console.log("+ :", num1 + num2);
console.log("- :", num1 - num2);
console.log("* :", num1 * num2);
console.log("/ :", num1 / num2);
console.log("% :", num1 % num2);
console.log("> :", num1 > num2);
console.log(">= :", num1 >= num2);
console.log("< :", num1 < num2);
console.log("<= :", num1 <= num2);
console.log("= :", num1 === num2);
console.log("!= :", num1 !== num2);
console.log("&& :", num1 && num2);
console.log("|| :", num1 || num2);
console.log("! :", !num2);

